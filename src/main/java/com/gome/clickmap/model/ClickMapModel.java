package com.gome.clickmap.model;

/**
 * 
 * @author chixiaoyong
 *
 */
public class ClickMapModel {

	private String x;
	
	private String y;
	
	private double clickCount;

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}

	public double getClickCount() {
		return clickCount;
	}

	public void setClickCount(double clickCount) {
		this.clickCount = clickCount;
	}

}
